  _cropImage(filePath) async {
    await ImageCropper.cropImage(
        sourcePath: filePath.path,
        maxWidth: 1024,
        maxHeight: 1024,
        cropStyle: CropStyle.circle,
        aspectRatio: CropAspectRatio(ratioX: 1.0, ratioY: 1.0)
    ).then((croppedImage){
      if (croppedImage  != null) {
        setState(() {
