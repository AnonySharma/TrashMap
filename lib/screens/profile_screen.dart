        });
      }
    });
  }

  _imgFromCamera() async {
    await _picker.getImage(
      source: ImageSource.camera, imageQuality: 50
    ).then((pickedfile){
      setState(() {
        if(pickedfile!=null)
          _image = File(pickedfile.path);
          _cropImage(_image);
      });
    });

    // print(_image);
  }

